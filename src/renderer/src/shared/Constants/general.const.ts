export const SHIFTS = {
  A: {
    starts: "06:00:00",
    ends: "14:59:59",
  },
  B: {
    starts: "15:00:00",
    ends: "23:59:59",
  },
  C: {
    starts: "00:00:00",
    ends: "05:59:59",
  },
};

export const getCurrentShift = (date: Date = new Date()): "A" | "B" | "C" => {
  const pad = (n: number) => n.toString().padStart(2, "0");
  const currentTime = `${pad(date.getHours())}:${pad(date.getMinutes())}:${pad(date.getSeconds())}`;

  const isBetween = (start: string, end: string) => {
    return start <= currentTime && currentTime <= end;
  };

  if (isBetween(SHIFTS.A.starts, SHIFTS.A.ends)) return "A";
  if (isBetween(SHIFTS.B.starts, SHIFTS.B.ends)) return "B";
  // If not A or B, then must be C
  return "C";
};
